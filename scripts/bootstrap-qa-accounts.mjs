import { createClient } from '@supabase/supabase-js';
import { existsSync, readFileSync, writeFileSync } from 'node:fs';
import { randomBytes } from 'node:crypto';

const ENV_FILES = ['.env.qa.local', '.env.local', '.env'];
const QA_ENV_FILE = '.env.qa.local';

function parseEnvFile(file) {
  if (!existsSync(file)) return {};
  const env = {};
  for (const row of readFileSync(file, 'utf8').split(/\r?\n/)) {
    const line = row.trim();
    if (!line || line.startsWith('#')) continue;
    const index = line.indexOf('=');
    if (index < 0) continue;
    const key = line.slice(0, index).trim();
    const rawValue = line.slice(index + 1).trim();
    env[key] = rawValue.replace(/^["']|["']$/g, '');
  }
  return env;
}

function loadLocalEnv() {
  for (const file of ENV_FILES) {
    const env = parseEnvFile(file);
    for (const [key, value] of Object.entries(env)) {
      if (process.env[key] == null) process.env[key] = value;
    }
  }
}

function quoteEnv(value) {
  return `"${String(value).replace(/\\/g, '\\\\').replace(/"/g, '\\"')}"`;
}

function saveQaEnv(values) {
  const existing = parseEnvFile(QA_ENV_FILE);
  const next = { ...existing, ...values };
  const ordered = [
    'QA_BUYER_EMAIL',
    'QA_BUYER_PASSWORD',
    'QA_SELLER_EMAIL',
    'QA_SELLER_PASSWORD',
    'QA_ADMIN_EMAIL',
    'QA_ADMIN_PASSWORD',
    'QA_ALLOW_PERSISTENT_WRITES',
  ];

  const lines = [
    '# Generated by npm run qa:bootstrap. This file is ignored by git.',
    ...ordered
      .filter(key => next[key] != null && next[key] !== '')
      .map(key => `${key}=${quoteEnv(next[key])}`),
  ];

  for (const [key, value] of Object.entries(next)) {
    if (!ordered.includes(key)) lines.push(`${key}=${quoteEnv(value)}`);
  }

  writeFileSync(QA_ENV_FILE, `${lines.join('\n')}\n`);
}

function makeClient(accessToken = null) {
  const url = process.env.VITE_SUPABASE_URL;
  const key = process.env.VITE_SUPABASE_PUBLISHABLE_KEY || process.env.VITE_SUPABASE_ANON_KEY;
  if (!url || !key) {
    throw new Error('Missing VITE_SUPABASE_URL and VITE_SUPABASE_PUBLISHABLE_KEY or VITE_SUPABASE_ANON_KEY.');
  }

  return createClient(url, key, {
    auth: { persistSession: false, autoRefreshToken: false },
    global: accessToken ? { headers: { Authorization: `Bearer ${accessToken}` } } : undefined,
  });
}

function readPostgresUrl() {
  if (process.env.SUPABASE_DB_URL || process.env.DATABASE_URL) {
    return process.env.SUPABASE_DB_URL || process.env.DATABASE_URL;
  }

  const credsPath = 'docs/superbase_creds.md';
  if (!existsSync(credsPath)) return null;
  const raw = readFileSync(credsPath, 'utf8');
  return raw.match(/postgresql:\/\/[^\s]+/)?.[0] || null;
}

function log(status, label, detail = '') {
  const suffix = detail ? ` - ${detail}` : '';
  console.log(`${status.padEnd(6)} ${label}${suffix}`);
}

function makePassword() {
  return `FormaQA-${randomBytes(12).toString('base64url')}!9`;
}

function makeEmail(role) {
  const url = process.env.VITE_SUPABASE_URL || '';
  const ref = url.match(/https:\/\/([^.]+)/)?.[1] || 'local';
  const stamp = new Date().toISOString().replace(/[-:TZ.]/g, '').slice(0, 14);
  const domain = process.env.QA_EMAIL_DOMAIN || 'gmail.com';
  return `forma.qa.${role}.${ref}.${stamp}@${domain}`;
}

async function signIn(email, password) {
  const authClient = makeClient();
  const { data, error } = await authClient.auth.signInWithPassword({ email, password });
  if (error) return { error };
  return {
    user: data.user,
    client: makeClient(data.session?.access_token),
  };
}

async function confirmAuthEmail(email) {
  const connectionString = readPostgresUrl();
  if (!connectionString) return false;

  const { Client } = await import('pg');
  const client = new Client({
    connectionString,
    ssl: { rejectUnauthorized: false },
  });

  try {
    await client.connect();
  } catch {
    return false;
  }

  try {
    const result = await client.query(
      `
        update auth.users
        set email_confirmed_at = coalesce(email_confirmed_at, now()),
            confirmed_at = coalesce(confirmed_at, now()),
            updated_at = now()
        where lower(email) = lower($1)
        returning id
      `,
      [email]
    );
    return result.rowCount > 0;
  } finally {
    await client.end();
  }
}

function assertQaEmail(email) {
  if (!String(email || '').toLowerCase().startsWith('forma.qa.')) {
    throw new Error(`Refusing direct auth seed for non-QA email: ${email}`);
  }
}

async function directCreateConfirmedUser({ role, email, password, displayName }) {
  assertQaEmail(email);

  const connectionString = readPostgresUrl();
  if (!connectionString) {
    throw new Error('Direct QA seed needs SUPABASE_DB_URL/DATABASE_URL or docs/superbase_creds.md.');
  }

  const { Client } = await import('pg');
  const client = new Client({
    connectionString,
    ssl: { rejectUnauthorized: false },
  });

  try {
    await client.connect();
  } catch (error) {
    throw new Error(
      `could not connect to Supabase Postgres (${error.message}). ` +
      'If the direct database host is IPv6-only on this machine, set SUPABASE_DB_URL to the Supabase IPv4 pooler connection string from Database settings.'
    );
  }

  try {
    await client.query('begin');

    const userResult = await client.query(
      `
        with existing as (
          select id
          from auth.users
          where lower(email) = lower($1)
        ),
        generated as (
          select coalesce((select id from existing), gen_random_uuid()) as id
        ),
        inserted as (
          insert into auth.users (
            instance_id,
            id,
            aud,
            role,
            email,
            encrypted_password,
            email_confirmed_at,
            confirmed_at,
            created_at,
            updated_at,
            raw_app_meta_data,
            raw_user_meta_data,
            is_super_admin,
            confirmation_token,
            recovery_token,
            email_change,
            email_change_token_new,
            email_change_token_current
          )
          select
            '00000000-0000-0000-0000-000000000000',
            generated.id,
            'authenticated',
            'authenticated',
            lower($1),
            crypt($2, gen_salt('bf')),
            now(),
            now(),
            now(),
            now(),
            '{"provider":"email","providers":["email"]}'::jsonb,
            $3::jsonb,
            false,
            '',
            '',
            '',
            '',
            ''
          from generated
          where not exists (select 1 from existing)
          returning id
        )
        select id from inserted
        union all
        select id from existing
        limit 1
      `,
      [
        email,
        password,
        JSON.stringify({ display_name: displayName, role }),
      ]
    );

    const userId = userResult.rows[0]?.id;
    if (!userId) throw new Error(`Could not create or find auth user for ${email}`);

    await client.query(
      `
        update auth.users
        set encrypted_password = crypt($2, gen_salt('bf')),
            email_confirmed_at = coalesce(email_confirmed_at, now()),
            confirmed_at = coalesce(confirmed_at, now()),
            raw_user_meta_data = coalesce(raw_user_meta_data, '{}'::jsonb) || $3::jsonb,
            raw_app_meta_data = '{"provider":"email","providers":["email"]}'::jsonb,
            updated_at = now()
        where id = $1
      `,
      [
        userId,
        password,
        JSON.stringify({ display_name: displayName, role }),
      ]
    );

    await client.query(
      `
        insert into auth.identities (
          id,
          user_id,
          provider_id,
          provider,
          identity_data,
          last_sign_in_at,
          created_at,
          updated_at
        )
        values (
          gen_random_uuid(),
          $1,
          $1::text,
          'email',
          jsonb_build_object(
            'sub', $1::text,
            'email', lower($2),
            'email_verified', true,
            'phone_verified', false
          ),
          now(),
          now(),
          now()
        )
        on conflict (provider, provider_id) do update
        set identity_data = excluded.identity_data,
            updated_at = now()
      `,
      [userId, email]
    );

    await client.query(
      `
        insert into public.profiles (id, email, display_name, role, verified)
        values ($1, lower($2), $3, $4, false)
        on conflict (id) do update
        set email = excluded.email,
            display_name = excluded.display_name,
            role = excluded.role,
            updated_at = now()
      `,
      [userId, email, displayName, role]
    );

    await client.query('commit');
  } catch (error) {
    await client.query('rollback');
    throw error;
  } finally {
    await client.end();
  }

  const retry = await signIn(email, password);
  if (retry.error) throw new Error(`direct QA user seed completed but sign-in failed: ${retry.error.message}`);
  const profile = await waitForProfile(retry.client, retry.user.id, role);
  log('PASS', `${role} direct seed/sign-in`, `${profile.role}${profile.verified ? ', verified' : ''}`);
  return { ...retry, profile, email, password };
}

async function waitForProfile(client, userId, label) {
  for (let attempt = 0; attempt < 8; attempt += 1) {
    const { data, error } = await client
      .from('profiles')
      .select('id, email, display_name, role, verified')
      .eq('id', userId)
      .maybeSingle();
    if (data) return data;
    if (error) throw new Error(`${label} profile lookup failed: ${error.message}`);
    await new Promise(resolve => setTimeout(resolve, 500));
  }
  throw new Error(`${label} profile was not created by the auth trigger.`);
}

async function ensurePasswordUser({ role, email, password, displayName }) {
  const existing = await signIn(email, password);
  if (!existing.error) {
    const profile = await waitForProfile(existing.client, existing.user.id, role);
    log('PASS', `${role} sign-in`, `${profile.role}${profile.verified ? ', verified' : ''}`);
    return { ...existing, profile, email, password };
  }

  const authClient = makeClient();
  const { data, error } = await authClient.auth.signUp({
    email,
    password,
    options: {
      data: {
        display_name: displayName,
        role,
      },
    },
  });

  if (error) {
    if (/rate limit|invalid/i.test(error.message || '')) {
      return directCreateConfirmedUser({ role, email, password, displayName });
    }
    throw new Error(`${role} signup failed: ${error.message}`);
  }

  const session = data.session;
  if (!session?.access_token) {
    const retry = await signIn(email, password);
    if (retry.error && /email not confirmed/i.test(retry.error.message || '')) {
      const confirmed = await confirmAuthEmail(email);
      if (confirmed) {
        const confirmedRetry = await signIn(email, password);
        if (!confirmedRetry.error) {
          const profile = await waitForProfile(confirmedRetry.client, confirmedRetry.user.id, role);
          log('PASS', `${role} signup/confirm/sign-in`, `${profile.role}${profile.verified ? ', verified' : ''}`);
          return { ...confirmedRetry, profile, email, password };
        }
        retry.error = confirmedRetry.error;
      }
    }
    if (retry.error) {
      try {
        return await directCreateConfirmedUser({ role, email, password, displayName });
      } catch (directError) {
        throw new Error(
          `${role} account was created but cannot sign in yet: ${retry.error.message}. ` +
          `Direct QA seed also failed: ${directError.message}. ` +
          'Disable email confirmation for QA, confirm the user in Supabase Auth, set a reachable SUPABASE_DB_URL, or use a pre-confirmed QA account.'
        );
      }
    }
    const profile = await waitForProfile(retry.client, retry.user.id, role);
    log('PASS', `${role} signup/sign-in`, `${profile.role}${profile.verified ? ', verified' : ''}`);
    return { ...retry, profile, email, password };
  }

  const client = makeClient(session.access_token);
  const profile = await waitForProfile(client, data.user.id, role);
  log('PASS', `${role} signup`, `${profile.role}${profile.verified ? ', verified' : ''}`);
  return { user: data.user, client, profile, email, password };
}

async function ensureSellerApproved(seller, admin) {
  if (seller.profile.role !== 'artist') {
    throw new Error('QA seller account must have role artist.');
  }

  const suffix = seller.user.id.replace(/-/g, '').slice(0, 8);
  const applicationPayload = {
    profile_id: seller.user.id,
    studio_name: 'FORMA QA Studio',
    handle: `forma-qa-studio-${suffix}`,
    city: 'QA Lab',
    bio: 'Dedicated verified seller account for production-readiness smoke tests.',
    portfolio_url: 'https://example.com/forma-qa-portfolio',
    process_notes: 'QA bootstrap application for validating seller onboarding, studio publishing, storage, and settlement visibility.',
    sample_works: [
      {
        title: 'QA process sample',
        imageUrl: 'https://example.com/forma-qa-sample.png',
        notes: 'Generated by the QA bootstrap script for admin approval testing.',
      },
    ],
    status: seller.profile.verified ? 'approved' : 'pending',
  };

  const { data: application, error: applicationError } = await seller.client
    .from('seller_applications')
    .upsert(applicationPayload, { onConflict: 'profile_id' })
    .select('id, status')
    .single();
  if (applicationError) throw new Error(`seller application upsert failed: ${applicationError.message}`);
  log('PASS', 'seller application', application.status);

  if (application.status !== 'approved') {
    const { error: reviewError } = await admin.client
      .from('seller_applications')
      .update({
        status: 'approved',
        review_note: 'Approved by QA bootstrap for automated production-readiness smoke tests.',
      })
      .eq('id', application.id);
    if (reviewError) throw new Error(`admin seller approval failed: ${reviewError.message}`);
    log('PASS', 'admin seller approval');
  }

  for (let attempt = 0; attempt < 10; attempt += 1) {
    const profile = await waitForProfile(seller.client, seller.user.id, 'seller');
    const { data: artist, error: artistError } = await seller.client
      .from('artists')
      .select('id, profile_id, verified')
      .eq('profile_id', seller.user.id)
      .maybeSingle();
    if (artistError) throw new Error(`seller artist lookup failed: ${artistError.message}`);
    if (profile.verified && artist?.id) {
      log('PASS', 'verified seller ready', artist.id);
      return { profile, artist };
    }
    await new Promise(resolve => setTimeout(resolve, 500));
  }

  throw new Error('Seller approval did not create a verified artist profile in time.');
}

async function main() {
  loadLocalEnv();

  const adminEmail = process.env.QA_ADMIN_EMAIL;
  const adminPassword = process.env.QA_ADMIN_PASSWORD;
  if (!adminEmail || !adminPassword) {
    throw new Error('Set QA_ADMIN_EMAIL and QA_ADMIN_PASSWORD before running qa:bootstrap.');
  }

  const sharedPassword = process.env.QA_BOOTSTRAP_PASSWORD || makePassword();
  const buyerEmail = process.env.QA_BUYER_EMAIL || makeEmail('buyer');
  const buyerPassword = process.env.QA_BUYER_PASSWORD || sharedPassword;
  const sellerEmail = process.env.QA_SELLER_EMAIL || makeEmail('seller');
  const sellerPassword = process.env.QA_SELLER_PASSWORD || sharedPassword;

  const admin = await signIn(adminEmail, adminPassword);
  if (admin.error) throw new Error(`admin sign-in failed: ${admin.error.message}`);
  admin.profile = await waitForProfile(admin.client, admin.user.id, 'admin');
  if (admin.profile.role !== 'admin') throw new Error('QA admin account must have role admin.');
  log('PASS', 'admin sign-in', `${admin.profile.role}${admin.profile.verified ? ', verified' : ''}`);

  const buyer = await ensurePasswordUser({
    role: 'buyer',
    email: buyerEmail,
    password: buyerPassword,
    displayName: 'FORMA QA Buyer',
  });
  if (buyer.profile.role !== 'buyer') throw new Error('QA buyer account must have role buyer.');

  const seller = await ensurePasswordUser({
    role: 'artist',
    email: sellerEmail,
    password: sellerPassword,
    displayName: 'FORMA QA Seller',
  });
  await ensureSellerApproved(seller, admin);

  const persistAdmin = process.env.QA_PERSIST_ADMIN === 'true';
  saveQaEnv({
    QA_BUYER_EMAIL: buyerEmail,
    QA_BUYER_PASSWORD: buyerPassword,
    QA_SELLER_EMAIL: sellerEmail,
    QA_SELLER_PASSWORD: sellerPassword,
    ...(persistAdmin ? {
      QA_ADMIN_EMAIL: adminEmail,
      QA_ADMIN_PASSWORD: adminPassword,
    } : {}),
    QA_ALLOW_PERSISTENT_WRITES: process.env.QA_ALLOW_PERSISTENT_WRITES || 'false',
  });

  log('DONE', 'QA accounts ready', `${buyerEmail}, ${sellerEmail}`);
  log('INFO', 'QA env updated', QA_ENV_FILE);
}

main().catch((error) => {
  log('FAIL', 'QA bootstrap', error.message);
  process.exitCode = 1;
});
